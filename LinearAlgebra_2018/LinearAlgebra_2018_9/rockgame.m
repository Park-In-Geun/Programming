        disp('You: PAPER    Computer:PAPER'); %사용자는 보, 컴퓨터는 보가 나왔다고 출력해줍니다.
        disp('A draw!'); %사용자와 컴퓨터가 비겼다고 출력해줍니다.
        a = a+1; %비겼기 때문에 게임이 끝나지 않은것으로 계산해주기 위해 입력받은 경기수에 1을 더해줍니다.
    elseif k==2 && t ==3  %k가 2(보) t가 3(가위)일 경우를 나타냅니다.
