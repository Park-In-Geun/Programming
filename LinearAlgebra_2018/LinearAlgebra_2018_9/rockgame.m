        l = l +1; %사용자가 패배하였기 때문에 l에 1을 더해줍니다.
    elseif k==3 && t ==1 %k가 3(가위) t가 1(바위)일 경우를 나타냅니다.
        disp('You: SCISSORS  Computer:ROCK'); %사용자는 가위, 컴퓨터는 바위가 나왔다고 출력해줍니다.
        disp('You lose!'); %사용자가 패배하였다고 출력해줍니다.
        l = l +1; %사용자가 패배하였기 때문에 l에 1을 더해줍니다.
    elseif k==3 && t ==2 %k가 3(가위) t가 2(보)일 경우를 나타냅니다.
        disp('You: SCISSORS Computer:PAPER'); %사용자는 가위, 컴퓨터는 보가 나왔다고 출력해줍니다.
        disp('You win!'); %사용자가 승리하였다고 출력해줍니다.
        w = w+1; %사용자가 승리하였기 때문에 w에 1을 더해줍니다.
    elseif k==3 && t ==3 %사용자는 가위, 컴퓨터는 가위가 나왔다고 출력해줍니다.
        disp('You: SCISSORS  Computer:SCISSORS');
